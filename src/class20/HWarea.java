package class20;

public class HWarea {
    public static void calculateArea(int length, int wigth){
        System.out.println("The area of rectangle is "+(length*wigth));
    }
    public static void calculateArea(int side){
        System.out.println("The area of square is "+(side*side));
    }
    public static void calculateVolume(int length, int wigth, int height){
        System.out.println("The volume of box is "+(length*wigth*height));
    }
}
